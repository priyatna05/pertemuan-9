    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int i=jTable1.getSelectedRow();
        if(i==-1)
        {
            return;
        }
        
                String id=(String) jTable1.getValueAt(i, 0);
                jTextField1.setText(id);
                String password=(String) jTable1.getValueAt(i, 1);
                jTextField2.setText(password);
                String nama=(String) jTable1.getValueAt(i, 2);
                if (nama.equals("Laki-laki")){
                    jRadioButton1.setSelected(true);
                } else if(nama.equals("Perempuan")){
                    jRadioButton2.setSelected(true);
                }
                String no=(String) jTable1.getValueAt(i, 3);
                jTextField4.setText(no);
                String email=(String) jTable1.getValueAt(i, 4);
                jTextField5.setText(email);
                String alamat=(String) jTable1.getValueAt(i, 5);
                jTextField6.setText(alamat);
    }//GEN-LAST:event_jTable1MouseClicked

