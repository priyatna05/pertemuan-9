     private void tabel(){ 
DefaultTableModel tab= new DefaultTableModel();
 tab.addColumn("ID"); 
 tab.addColumn("Nama"); 
 tab.addColumn("Jenis Kelamin");
 tab.addColumn("No HP");
 tab.addColumn("Alamat");
 tab.addColumn("Email");

 
 jTable1.setModel(tab); 
 try{ res=stat.executeQuery("select * from pelanggan"); 
 while (res.next()) { 
         Object[] o=new Object[6];
         o[0]=res.getString("id_pelanggan");
         o[1]=res.getString("nama"); 
         o[2]=res.getString("Jenis_Kelamin");
         o[3]=res.getString("no_tlp");
         o[4]=res.getString("email");
         o[5]=res.getString("alamat");
         tab.addRow(o);
 }
 }catch (SQLException e) { 
 JOptionPane.showMessageDialog(rootPane, e); 
 } 
}
