    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int i=table.getSelectedRow();
        if(i==-1)
        {
            return;
        }
        
                String ID=(String) table.getValueAt(i, 0);
                IdRumah.setText(ID);
                String Nama=(String) table.getValueAt(i, 1);
                NamaRumah.setText(Nama);
                String harga=(String) table.getValueAt(i, 2);
                HargaRumah.setText(harga);
                String jenis=(String) table.getValueAt(i, 3);
                KatagoriRumahComboBox.setSelectedItem(jenis);
                String stok=(String) table.getValueAt(i, 4);
                StokRumah.setText(stok);
                String gambar=(String) table.getValueAt(i, 5);
                Gambar.setText(gambar);
                jLabel4.setText("");
        ImageIcon icon = new ImageIcon(gambar);
        jLabel4.setIcon(icon);
        
    }//GEN-LAST:event_tableMouseClicked

