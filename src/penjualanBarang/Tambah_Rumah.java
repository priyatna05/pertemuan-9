    private void tabel(){ 
DefaultTableModel tab= new DefaultTableModel();
 tab.addColumn("id_rumah"); 
 tab.addColumn("nama_rumah"); 
 tab.addColumn("harga_rumah");
 tab.addColumn("katagori_rumah");
 tab.addColumn("stok_rumah");
 tab.addColumn("gambar_rumah");

 
 table.setModel(tab); try{ res=stat.executeQuery("select * from tabel_rumah"); 
 while (res.next()) { 
  Object[] o=new Object[6];
  o[0]=res.getString("id_rumah");
  o[1]=res.getString("nama_rumah"); 
  o[2]=res.getString("harga_rumah") ;
  o[3]=res.getString("katagori_rumah");
  o[4]=res.getString("stok_rumah");
  o[5]=res.getString("gambar_rumah");
  tab.addRow(o);
 
 }
 
 }catch (SQLException e) { 
 JOptionPane.showMessageDialog(rootPane, e); 
 } 
}
